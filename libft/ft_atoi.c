int		atoi(char *str)
{
	int	i;
	int	j;
	int	k;

	k = 1;
	i = 0;
	j = 0;
	while (str[i] == ' ')
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		k = 1 - 2 * (str[i++] == '-');
	}
	if (str[i] >= '0' && str[i] <= '9')
	{
		while (str[i] >= '0' && str[i] <= '9')
		{
			j = j * 10 + (str[i++] - 48);
		}
	}
	return (j * k);
}
