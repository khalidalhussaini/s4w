int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 1;
	while (((*s1 != '\0') || (*s2 != '\0')) && i <= n)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		i++;
		s2++;
		s1++;
	}
	return (0);
}
